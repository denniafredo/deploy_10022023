    <p><b><font face="Verdana" size="2">Cara mengubah Regional Setting :</font></b></p>
    <ul>
      <li><font face="Verdana" size="2"><b>Untuk Windows XP</b><br>
      1. Klik <i>Start</i> -&gt; <i>Control Panel</i> -&gt; Klik icon <i>Regional and Language</i><br>
      2. Pilih Tab <i>Regional Options</i>, pada bagian <i>Standards and formats</i> Pilih 
      <i>Indonesian</i></font></li>
      <li><font face="Verdana" size="2"><b>Untuk Windows 2000</b><br>
      1. Klik <i>Start</i> -&gt; <i>Settings</i> -&gt; <i>Control Panel</i> -&gt; <i>Regional Options</i><br>
      2. Pilih Tab <i>General</i>. Pada Bagian <i>Settings for the current user</i> pilih 
      <i>Indonesian</i></font></li>
      <li><font face="Verdana" size="2"><b>Untuk Windows 98/SE/ME</b><br>
      1. Klik <i>Start</i> -&gt; <i>Settings</i> -&gt; <i>Control Panel</i>-&gt; <i>Regional Settings</i><br>
      2. Pilih Country <i>Indonesia</i></font></li>
